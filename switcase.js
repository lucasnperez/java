let fruta = "banana";

switch (fruta) {
        case "melancia": 
        console.log("A casca da fruta é verde");
        break;
        case "abobora":
        console.log("A casca da fruta é laranja");
        break;
        case "maçã":
        console.log("A casca de fruta é vermelha");
        break;
        case "banana":
        console.log("A casca da fruta é amarela");
        break;
default:
    console.log("Essa fruta não esta na lista")
    break;
}